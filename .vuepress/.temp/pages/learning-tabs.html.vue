<p>We are living in an increasingly interconnected digital world. As such, the complexities of managing and creating applications that operate in this domain become ever more complex. Workflows and techniques that may have been adequate a few years ago quickly become redundant. Web-application developers, to manage this ever-changing world, are adopting an agile approach to software development. Such an approach means quick iterations and a flexible development infrastructure that supports this process. The focus of this unit will be to explore how this process is managed. In doing so you will be exposed to work flows and modern development toolkits. Theory will be conceptualised and used on realistic projects that will be implemented to commercial production standards.</p>

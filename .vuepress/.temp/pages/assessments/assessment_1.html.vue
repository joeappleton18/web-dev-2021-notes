<h2 id="assessment-task" tabindex="-1"><a class="header-anchor" href="#assessment-task" aria-hidden="true">#</a> Assessment Task</h2>
<p>The practical outcomes for this unit are to create a responsive single page web-application supporting students in their university experience. This idea has been proposed by our industry partners - Dootrix. They will be presenting their ideas in the first lecture of the term so it is vital you attend.</p>
<p>This assessment represents the exploratory phase of the wider project. Specifically, you are trying to find out in what ways students are underserved in their day-to-day university lives and how a web-application could help. Your findings, proposed solution and user feedback of the solution will form the basis of this current assessment.</p>
<h2 id="deliverables" tabindex="-1"><a class="header-anchor" href="#deliverables" aria-hidden="true">#</a> Deliverables</h2>
<p>You are required to deliver an academic styled report. As such, you should ensure you write in a formal style. Furthermore, you should use high-quality references to underpin your work.</p>
<p>You will submit the completed piece of work as a single PDF file.</p>
<h2 id="report-structure" tabindex="-1"><a class="header-anchor" href="#report-structure" aria-hidden="true">#</a> Report Structure</h2>
<h2 id="_1-0-introduction" tabindex="-1"><a class="header-anchor" href="#_1-0-introduction" aria-hidden="true">#</a> 1. 0 Introduction</h2>
<p>This section should provide a background to the problem that you are addressing, the approach you used to explore the problem and your key finding. Finding good quality sources to underpin your arguments strengthen this section.</p>
<h2 id="_2-0-methods-methodology" tabindex="-1"><a class="header-anchor" href="#_2-0-methods-methodology" aria-hidden="true">#</a> 2. 0 Methods/Methodology</h2>
<h3 id="_2-1-overarching-methodology" tabindex="-1"><a class="header-anchor" href="#_2-1-overarching-methodology" aria-hidden="true">#</a> 2.1 Overarching Methodology</h3>
<p>You should provide a summary of the overarching methodology that you are using. Dootrix recommends using a design thinking methodology to conduct this exploratory phase of the project. You are, however, free to choose a different methodology if it better fits your needs. Moreover, you may also cherry-pick elements of multiple methodologies.  Regardless of the methodology you use, it must be clear that you have engaged with key sources and taken an organised, systematic approach to follow your given methodology.</p>
<h3 id="_2-2-methods" tabindex="-1"><a class="header-anchor" href="#_2-2-methods" aria-hidden="true">#</a> 2.2 Methods</h3>
<p>Methods can be considered the tasks, tools and techniques that you used to gain an understanding of the proposed problem and allowed you to present and test a solution. Within this section, you will outline the methods you used.</p>
<p>The methods will be very much dictated by the methodology that you have chosen. What is key, is you will need to test your ideas on a representative base of students. Typically, you will follow some sort of iterative process, where you engage with student demographics to define the problem and determine how they are underserved. Next, you will develop a simple prototype using a tool such as Invision or Figma to test your ideas on this student demographic.</p>

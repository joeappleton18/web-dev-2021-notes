<h2 id="assessment-task" tabindex="-1"><a class="header-anchor" href="#assessment-task" aria-hidden="true">#</a> Assessment Task</h2>
<p>The practical outcomes for this unit are to create a responsive single page web-application (SPA) that supports Solent University students in some aspect of their day-to-day lives. To this end, over the course of two assessments you will use a data driven approach to identify a specific area in which students are being underserved and implement a prototype SPA to better serve students in your identified problem space.</p>
<p><img src="@source/assessments/design_thinking_process_assessment_2.png" alt="design thinking process"></p>
<p><strong>Figure 1</strong>, <a href="https://www.interaction-design.org/literature/article/5-stages-in-the-design-thinking-process" target="_blank" rel="noopener noreferrer">The design thinking methodology, adapted from Interaction Design Foundation (n.d.).<OutboundLink/></a></p>
<p>In using the design thinking (DT) methodology as a lens (see Figure 1), this assessment represents the empathise, define and ideate phases of the overarching DT methodology. In other words, you will identify a problem in students every-day lives (empathise), clearly define this problem (define) and propose a solution (ideate).</p>
<h2 id="deliverables" tabindex="-1"><a class="header-anchor" href="#deliverables" aria-hidden="true">#</a> Deliverables</h2>
<p>You are required to deliver a, 2,500 word academic styled report that should include the section presented below.</p>
<h2 id="report-structure" tabindex="-1"><a class="header-anchor" href="#report-structure" aria-hidden="true">#</a> Report Structure</h2>
<h2 id="_1-0-introduction" tabindex="-1"><a class="header-anchor" href="#_1-0-introduction" aria-hidden="true">#</a> 1. 0 Introduction</h2>
<p>This section should provide a background to the problem that you are addressing, the approach you used to explore the problem and your key findings.</p>
<h2 id="_2-0-methods-methodology" tabindex="-1"><a class="header-anchor" href="#_2-0-methods-methodology" aria-hidden="true">#</a> 2. 0 Methods/Methodology</h2>
<h3 id="_2-1-overarching-methodology" tabindex="-1"><a class="header-anchor" href="#_2-1-overarching-methodology" aria-hidden="true">#</a> 2.1 Overarching Methodology</h3>
<p>You should provide a summary of the overarching methodology that you are using, I recommend using DT. However, you are free to choose a different methodology if it better meets your needs. You can even cherry-pick elements of multiple methodologies. Regardless of the methodology you use, it must be clear that you have engaged with key sources and taken an organised, systematic approach in following your choice. Further to this, you must clearly argue why the given methodology is suitable for your project.</p>
<h3 id="_2-2-methods" tabindex="-1"><a class="header-anchor" href="#_2-2-methods" aria-hidden="true">#</a> 2.2 Methods</h3>
<p>You should present the methods that you used. Methods can be considered the tasks, tools and techniques that you used to gain an understanding of the proposed problem and allowed you to hypothesise a solution (e.g. interviews, focus group, surveys or observations). You may use a single method (e.g. interview, focus groups or surveys) or several. Given the scope of the project, I am expecting a sample of around 5 to 10 students for focus groups/interviews, and 20+ for surveys.</p>
<h2 id="_3-0-ethics" tabindex="-1"><a class="header-anchor" href="#_3-0-ethics" aria-hidden="true">#</a> 3.0 Ethics</h2>
<p>You should present a brief account of the ethical considerations and acknowledgment that you have gained ethical clearance.</p>
<h2 id="_4-0-discussion-and-results" tabindex="-1"><a class="header-anchor" href="#_4-0-discussion-and-results" aria-hidden="true">#</a> 4.0 Discussion and Results</h2>
<p>This section presents the finding and outcomes of the methodology and methods that you employed. The primary focus will be on the outcomes of user testing.</p>
<h2 id="_4-1-prototype" tabindex="-1"><a class="header-anchor" href="#_4-1-prototype" aria-hidden="true">#</a> 4.1 Prototype</h2>
<p>Based off your results, you should to present your prototype and clearly hypothesises how it solves the problem. You should use a tool such as <a href="https://www.figma.com/" target="_blank" rel="noopener noreferrer">Figma<OutboundLink/></a> to present me with a high-quality design of a single page web application. This solution should be informed by your data collection and analysis and should endeavoured to solve the problem that you identified.</p>
<h2 id="_5-0-conclusion" tabindex="-1"><a class="header-anchor" href="#_5-0-conclusion" aria-hidden="true">#</a> 5.0 Conclusion</h2>
<p>Conclude your overall findings, and propose a clear forward facing path.</p>

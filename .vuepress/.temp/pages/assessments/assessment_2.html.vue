<h2 id="assessment-task" tabindex="-1"><a class="header-anchor" href="#assessment-task" aria-hidden="true">#</a> Assessment Task</h2>
<p>The practical outcomes for this unit are to create a responsive single page web-application supporting students in their university experience.  This idea has been proposed by our industry partners - Dootrix.<br>
This assessment represents the delivery phase of the project.  You can focus on two routes design and development (see below). Regardless of the route chosen, a single page JavaScript application tested with users will be delivered. Supporting this delivery will be 2000 words of documentation outlining the process.</p>
<h2 id="deliverables" tabindex="-1"><a class="header-anchor" href="#deliverables" aria-hidden="true">#</a> Deliverables</h2>
<p>As part of your implementation your should produce a <code>documentation.md</code> file which will be 2000 words in length (+/- 10%), it will document and justify the choices that you made throughout the project. It does not need to be academic in style, however, it should not be chatty. In line with the style, instead of citations you should use hyperlinks. Importantly, you must show that you followed a methodology. For instance, if you are using agile, your user stories and interactions will be documented - beyond this, the structure is a fee choice. <code>documentation.md</code> will be exported as a pdf and uploaded to SOL.</p>
<h2 id="focus" tabindex="-1"><a class="header-anchor" href="#focus" aria-hidden="true">#</a> Focus</h2>
<p>You are free to pursue two different routes - design  and development.</p>
<h3 id="development-focused" tabindex="-1"><a class="header-anchor" href="#development-focused" aria-hidden="true">#</a> Development Focused</h3>
<p>If you choose this route, your solution should be a modern single page web application (SPA) conforming to professional standards. Design is still important and should not be overlooked, however, the primary focus is show technical rigour and elegance in your solution.  Pursuing this route will require you to demonstraight:</p>
<ul>
<li>A professional README.md file</li>
<li>Database persistence</li>
<li>Authentication</li>
<li>Unit testing (advanced)</li>
<li>High fidelity mock ups (these do not need to be validated with your audience)</li>
<li>Cloud function (advanced)</li>
<li>Evidence that the final solution has been evaluated by sample users</li>
</ul>
<h3 id="design-focused" tabindex="-1"><a class="header-anchor" href="#design-focused" aria-hidden="true">#</a> Design Focused</h3>
<p>Should you choose this route, you will still be creating single page web application, however, the main focus is to demonstrate and validate conceptual design ideas. As such, you may not need to make API calls, and interact with a database. Instead,  you can use place holder data to create a proof of concept. Pursuing this route will require you to demonstraight:</p>
<ul>
<li>A professional README.md file</li>
<li>Multiple interactions of  user research to validate your prototype (e.g. interviews, surveys and focus groups). This must be done with a realistic use-base</li>
<li>Design inspiration</li>
<li>Extremely high quality and unique designs have been implemented into a single page web application.</li>
<li>Hi-Fidelity Wire Frame</li>
</ul>
